#THE MECHANISM OF ITERATING THROUGH SEQUENCE DATA TYPES IS CALLED LOOPING.

#------------------------------------------------------------#

#FOR LOOP

#STRING DATATYPE
string_variable = "Ragosh Shrestha"
print(string_variable)
for character in string_variable:
    print(character)
print()
print()

#------------------------------------------------------------#

#LIST DATATYPE
list_variable = ["Ragosh","Shrestha"]
print(list_variable)
for item in list_variable:
    print(item)
print()
print()

#------------------------------------------------------------#

#RANGE
a = list(range(10))
print(a)
for i in a:
    print(i)
print()
print()

#------------------------------------------------------------#

#WHILE LOOP

a = 0
while (a < 5):
    print(a)
    a += 1
print("Nooooooo")

